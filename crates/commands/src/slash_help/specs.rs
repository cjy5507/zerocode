//! - [`SLASH_COMMAND_SPECS`] — every spec the CLI knows about, in canonical
//!   display order.
//! - [`LOW_VALUE_DEFERRED_SLASH_COMMANDS`] — commands intentionally hidden
//!   from the default help/suggest surface (low signal, kept for
//!   compatibility).
